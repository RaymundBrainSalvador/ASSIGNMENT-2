# DOT METHOD

# EXAMPLE A
import numpy as np

a = np.array([[2.5, 3.7], [4.9, 5.1]])
b = np.array([[1, 2], [3, 4]])

result = np.dot(a,b)

print(f'EXAMPLE A:{result}')

